# real_user
if [[ -z $RL_USR ]] ;
then

	[[ "$SUDO_USER" ]] \
	&& declare -r RL_USR="$SUDO_USER" \
	|| declare -r RL_USR="$USER" \
	;

fi ;


# real_home
[[ -z $RL_HM ]] \
&& declare -r RL_HM="` \
	getent \
		passwd \
		"$RL_USR" \
	| cut \
		-d: \
		-f6 \
`" \
;
