#!/usr/bin/env bash
# ######################################################################################
# GIT CACHE CONTROLLER UTILITY GROUP
# ######################################################################################
GitCcCtlr() {


# ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
# CORE STATIC PUBLIC FUNCTION
# ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
# ======================================================================================
# APPEND
# ======================================================================================
	GitCcCtlr__apd() {

		...

	} ;



# ======================================================================================
# APPLY
# ======================================================================================
	GitCcCtlr__aply() {

		...

	} ;



# ======================================================================================
# DELETE
# ======================================================================================
	GitCcCtlr__dl() {

		...

	} ;



# ======================================================================================
# HAS
# ======================================================================================
	GitCcCtlr__hs() {

		...

	} ;



# ======================================================================================
# REMOVE
# ======================================================================================
	GitCcCtlr__rm() {

		...

	} ;



# ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
# DEFAULT STATIC PUBLIC FUNCTION
# ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
# ======================================================================================
# CONSTRUCTOR
# ======================================================================================


	# [ arguments ]
	local _ax=("$@") ;

	# [ git_tier1_named_arguments ]
	local -a _git_t1_nax ;


	# HELP --------

	s_isHlp \
		"$@" \
	&& echo "" \
	&& return $tru \
	;


	# VERSION --------

	s_isVer \
		"$@" \
	&& echo "" \
	&& return $tru \
	;


	# POSITIONED ARGUMENT --------

	# [ positoined_arguments ]
	local -a _pax=` \
		spairaru \
			cbl \
				pax \
	` ;


	# [ method ]
	local mtd__="${_pax[0]}" ;


	# MAIN PROCESS : METHOD --------

	# [ method ]
	local _mtd ;


	case "$mtd__" in


		# CORE

		apd | append ) _mtd=GitCcCtlr__apd ;;

		aply | apply ) _mtd=GitCcCtlr__aply ;;

		dl | delete ) _mtd=GitCcCtlr__dl ;;

		hs | has ) _mtd=GitCcCtlr__hs ;;

		rm | remove ) _mtd=GitCcCtlr__rm ;;


	esac ;


	# - method is not empty
	if [[ "$_mtd" ]] ;
	then


		shift 1 \
		&& "$_mtd" \
			"$@" \
		;


		return $? ;


	fi ;


	# POST PROCESS : REPORT STATUS --------

	return $fls ;


} ;
